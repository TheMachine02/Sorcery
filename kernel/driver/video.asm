;include 'include/kernel.inc'

define DRIVER_VIDEO_VRAM                  0xD40000
define DRIVER_VIDEO_VRAM_SIZE             0x25800
define DRIVER_VIDEO_FRAMEBUFFER_SIZE      0x12C00
define DRIVER_VIDEO_CTRL                  0xE30018
define DRIVER_VIDEO_CTRL_DEFAULT          100100100111b
define DRIVER_VIDEO_IMSC                  0xE3001C
define DRIVER_VIDEO_IMSC_DEFAULT          00000100b
define DRIVER_VIDEO_ICR                   0xE30028
define DRIVER_VIDEO_ISR                   0xE30020
define DRIVER_VIDEO_SCREEN                0xE30010
define DRIVER_VIDEO_BUFFER                0xE30014
define DRIVER_VIDEO_PALETTE               0xE30200
define DRIVER_VIDEO_TIMING0               0xE30000
define DRIVER_VIDEO_TIMING1               0xE30004
define DRIVER_VIDEO_TIMING2               0xE30008
define DRIVER_VIDEO_TIMING3               0xE3000C

define DRIVER_VIDEO_FONT_PALETTE          0xE30C08
define DRIVER_VIDEO_BLIT_PALETTE          0xE30C09

define DRIVER_VIDEO_IRQ                   00100000b
define DRIVER_VIDEO_IRQ_LOCK              0xE30C0A
define DRIVER_VIDEO_IRQ_LOCK_THREAD       0xE30C0B
define DRIVER_VIDEO_IRQ_LOCK_SET          0

kvideo:
	db	21
.jump:
	jp	.init
	jp	.irq_handler
	jp	.irq_lock
	jp	.irq_unlock
	jp	.swap
	jp	.vsync
	jp	.clear
	jp	.clear_color
	jp	.pixel_setup
	jp	.font_setup
	jp	.put_char
	jp	.put_string
	jp	.put_int
	jp	.put_hex
	jp	.char_width
	jp	.set_pixel
	jp	.get_pixel
	jp	.blit_area

.init:
	tstdi
	ld	hl, .PALETTE_WHITE
	ld	de, DRIVER_VIDEO_PALETTE+496
	ld	bc, 16
	ldir
	ld	a, 248
	ld	(DRIVER_VIDEO_FONT_PALETTE), a
	xor	a, a
	ld	(DRIVER_VIDEO_BLIT_PALETTE), a
	ld	hl, DRIVER_VIDEO_IMSC
	ld	(hl), DRIVER_VIDEO_IMSC_DEFAULT
	ld	hl, DRIVER_VIDEO_CTRL_DEFAULT
	ld	(DRIVER_VIDEO_CTRL), hl
	ld	hl, DRIVER_VIDEO_VRAM
	ld	(DRIVER_VIDEO_SCREEN), hl
	ld	hl, DRIVER_VIDEO_VRAM + DRIVER_VIDEO_FRAMEBUFFER_SIZE
	ld	(DRIVER_VIDEO_BUFFER), hl
; setup timings
	ld	de, DRIVER_VIDEO_TIMING0 + 1
	ld	de, .LCD_TIMINGS
	ld	bc, 8
	ldir
; clear the LCD
	call	.clear
; IRQ handle
	call	.irq_lock_reset
	ld	hl, .irq_handler
	ld	a, DRIVER_VIDEO_IRQ
	call	kirq.request
	retei

.irq_handler:
	ld	hl, DRIVER_VIDEO_ICR
	set	2, (hl)
	ld	hl, DRIVER_VIDEO_IRQ_LOCK
	bit	DRIVER_VIDEO_IRQ_LOCK_SET, (hl)
; carry flag is untouched, meaning that this IRQ will need a thread wake    
	ret	z
	ld	iy, (DRIVER_VIDEO_IRQ_LOCK_THREAD)
	push	af
	ld	a, (iy+KERNEL_THREAD_IRQ)
	tst	a, DRIVER_VIDEO_IRQ
	ld	(iy+KERNEL_THREAD_IRQ), 0
	call	nz, kthread.resume
	pop	af
; reset the carry flag, IRQ doesn't need thread to be waked
	ccf
	ret

.irq_lock:
	ld	hl, DRIVER_VIDEO_IRQ_LOCK
	sra	(hl)
	jr	nc, .irq_lock_acquire
	call	kthread.yield
	jr	.irq_lock
.irq_lock_acquire:
; note that an interrupt could occur between the jump and the load
; it is equivalent to an other thread trying to read the member of the mutex
; which lead to undefined behaviour
; However, since the lock reset itself to NULL, if such interrupt occur, the member will be read as NULL
; Waking up a null thread is a no-op
; An other thing to consider is even trying to wake the thread make no sense because it is already active
; In sake a simplicity, take no further action
	ld	hl, (kthread_current)
	ld	(DRIVER_VIDEO_IRQ_LOCK_THREAD), hl
	ret

.irq_unlock:
; does we own it ?
	ld	hl, (kthread_current)
	ld	de, (DRIVER_VIDEO_IRQ_LOCK_THREAD)
	or	a, a
	sbc	hl, de
; nope, bail out
	ret	nz
; set them freeeeee

.irq_lock_reset:
	ld	hl, DRIVER_VIDEO_IRQ_LOCK_THREAD
	ld	de, NULL
	ld	(hl), de
	dec	hl
	ld	(hl), KERNEL_MUTEX_MAGIC
	ret
    
.swap:
	tstdi
	ld	hl, (DRIVER_VIDEO_SCREEN) 
	ld	de, (DRIVER_VIDEO_BUFFER)
	ld	(DRIVER_VIDEO_BUFFER), hl
	ld	(DRIVER_VIDEO_SCREEN), de
	pop	af
; is interrupt enabled ?
; quit if not enabled, wait for vsync signal else
	ret	po
	
.vsync:
; We need to write to IRQ
	ld	a, DRIVER_VIDEO_IRQ
	jp	kthread.wait_on_IRQ

.clear:
	ld.atomic de, (DRIVER_VIDEO_BUFFER)
	ld	hl, 0xE40000
	ld	bc, 76800
	ldir
	ret
	
.clear_color:
	ld.atomic de, (DRIVER_VIDEO_BUFFER)
	or	a, a
	sbc	hl, hl
	add	hl, de
	inc	de
	ld	(hl), c
	ld	bc, 76799
	ldir
	ret

.pixel_setup:
	ld	d, 160
	mlt	de
	add	hl, de
	add	hl, de
	ld.atomic de, (DRIVER_VIDEO_BUFFER)
	add	hl, de
	ret

.font_setup:
	ld	d, 160
	mlt	de
	add	hl, de
	add	hl, de
	ld.atomic de, (DRIVER_VIDEO_BUFFER)
	add	hl, de
	ld	a,(DRIVER_VIDEO_FONT_PALETTE)
	ld	iyl, a
	ex	de, hl
	ret

.put_char:
; rasterise a font value
; c = char, e,hl  = xy
	call	.font_setup
.put_char_no_video_init:
	push	ix
	or	a, a
	sbc	hl, hl
	ld	l, c
	add	hl, hl
	add	hl, hl
	ld	bc, .TRANSLATION_TABLE
	add	hl, bc
	ld	hl, (hl)
; hl = font.adress (vstart offset, vsize (bytes), hsize(bytes))
; de = buffer adress
	ld	c, (hl)	; voffset
	ld	b, 160
	mlt	bc
	ex	de, hl
	add	hl, bc
	add	hl, bc	; voffset*320+buffer
	ex	de, hl
	inc	hl
	ld	ix, (hl)
	ld	b, 0		; quick micro opt, bcu=0 from mlt
	ld	c, ixh
	push	bc
	inc	b
	ld	c, 64
	inc	hl
	inc	hl
	ex	de, hl
.charvloop:
	push	hl
	ld	b, ixh
.charhloop:
	ld	a, (de)
	cp	a, 8
	jr	z, .charavd
	add	a, iyl
	ld	(hl), a
.charavd:
	inc	hl
	inc	de
	djnz	.charhloop
	pop	hl
	inc	b
	add	hl, bc
	dec	ixl
	jr	nz, .charvloop
	pop	hl
	pop	ix
	ret

.put_string:
; bc = string, e,hl  = xy
	call	.font_setup
.string_loop:
	ld	a, (bc)
	or	a, a
	ret	z
	push	de
	push	bc
	ld	c, a
	call	.put_char_no_video_init
	pop	bc
	pop	de
; add hl and hfont
	add	hl, de
	ex	de, hl
	inc	bc
	jr	.string_loop

.put_int:
; bc = int, e,hl  = xy, a = number of space to take (max 8)
	ld	a, 8 
.put_int_formated:
	push	ix
	push	bc
	ld	c, a
	call	.pixel_setup
	ld	e, a
	ld	a, 8
	sub	a, e
	jr	nc, $+3
	xor	a, a
	ld	e, a
	ld	d, 3
	mlt	de
	ld	ix, .TABLE_OF_TEN
	add	ix, de
	ex	de, hl
	pop	hl
	ld	a,(DRIVER_VIDEO_FONT_PALETTE)
	ld	iyl, a
	ld	a, c
; ix = table, hl = number, de = adress, a = loop value
.put_intloop:
	push	af
	ld	bc, (ix+0)
	lea	ix, ix+3
	ld	a,'0'-1
.find_digit:
	inc	a
	or a, a
	sbc	hl, bc
	jr	nc, .find_digit
	add	hl, bc
	push	hl
	push	de
	ld	c, a
	call	.put_char_no_video_init
	pop	de
	add	hl, de
	ex	de, hl
	pop	hl
	pop	af
	dec	a
	jr	nz, .put_intloop
	pop	ix
	ret

.put_hex:
; bc = number to blit in hex format [6 characters]
	call	.font_setup
; display bcu, then b, then c
	push	ix
	push	bc
	ld	ix, 0
	add	ix, sp
	ld	c, (ix+2)
	call	.put8bitDigit
	ld	c, (ix+1)
	call	.put8bitDigit
	ld	c, (ix+0)
	call	.put8bitDigit
	pop	bc
	pop	ix
	ret
.put8bitDigit:
; input c
	ld  a,c
	push	af
	rra
	rra
	rra
	rra
	call  .conv4bitDigit
	pop	af
.conv4bitDigit:
	and  $0F
	add  a,$90
	daa
	adc  a,$40
	daa
	ld	c, a
	push	de
	call .put_char_no_video_init
	pop	de
	add	hl, de
	ex	de, hl
	ret

.char_width:
; c = char, a = width
	or	a, a
	sbc	hl, hl
	ld	l, c
	add	hl, hl
	add	hl, hl
	ld	bc, .TRANSLATION_TABLE
	add	hl, bc
	ld	hl, (hl)
	inc	hl
	inc	hl
	ld	a, (hl)
	ret

.set_pixel:
; c = color, e,hl  = xy
	call	.pixel_setup
	ld	(hl), c
	ret

.get_pixel:
; a = outcolor, e,hl  = xy
	call	.pixel_setup
	ld	a, (hl)
	ret

.blit_area:
; hl,e ; bc is data as hsize,vsize,data
	call	.pixel_setup
	ld	a, (DRIVER_VIDEO_BLIT_PALETTE)
	ld	iyl, a
	ld	a, (bc)
	ld	e, a
	inc	bc
	ld	a, (bc)
	inc	bc
	push	bc
	inc.s	bc	; quick null ubc
	ld	c, a
	ld	b, e
	pop	de
.blit_vloop:
	push	bc
	push	hl	
.blit_hloop:
	ld	a, (de)
	or	a, a
	jr	z, .blit_continue
	add	a, iyl
	ld	(hl), a
.blit_continue:
	inc	hl
	inc	de
	djnz	.blit_hloop
	pop	hl
	inc	b
	ld	c, 64
	add	hl, bc
	pop	bc
	dec	c
	jr	nz, .blit_vloop
	ret

